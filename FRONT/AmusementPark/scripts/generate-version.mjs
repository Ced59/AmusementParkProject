import { execFileSync } from 'node:child_process';
import { mkdirSync, rmSync, writeFileSync } from 'node:fs';
import { dirname, relative, resolve } from 'node:path';
import { fileURLToPath } from 'node:url';

import { normalizeReleaseVersion, readConfiguredReleaseDocument } from './release-version-utils.mjs';

const fallbackVersion = 'v0.0.0-dev';
const scriptDirectory = dirname(fileURLToPath(import.meta.url));
const projectRoot = resolve(scriptDirectory, '..');
const outputPath = resolve(projectRoot, 'src', 'environments', 'version.generated.ts');
const summaryOutputPath = resolve(projectRoot, 'src', 'environments', 'release-history-summary.generated.ts');
const loadersOutputPath = resolve(projectRoot, 'src', 'environments', 'release-history-loaders.generated.ts');
const groupsOutputDirectory = resolve(projectRoot, 'src', 'environments', 'release-history-groups');
const explicitVersion = process.env.SITE_VERSION?.trim();
const configuredReleaseDocument = readConfiguredReleaseDocument(projectRoot);
const configuredVersion = configuredReleaseDocument ? normalizeReleaseVersion(configuredReleaseDocument.version) : null;
const configuredReleaseHistory = normalizeReleaseHistory(configuredReleaseDocument?.history);

const version = explicitVersion || configuredVersion || readGitTagVersion() || fallbackVersion;
const source = explicitVersion
  ? 'SITE_VERSION'
  : configuredVersion
    ? 'release-version.json'
    : version === fallbackVersion
      ? 'fallback'
      : 'git tag';
const releaseHistoryTree = buildReleaseHistoryTree(configuredReleaseHistory);
const content = `// This file is generated by scripts/generate-version.mjs.\nexport interface ReleaseHistoryEntry {\n  readonly version: string;\n  readonly releasedAt: string;\n  readonly labels: Record<string, string>;\n}\n\nexport const siteVersion = ${JSON.stringify(version)};\n`;
const summaryContent = `// This file is generated by scripts/generate-version.mjs.\nimport type { ReleaseHistoryEntry } from './version.generated';\n\nexport type ReleaseHistorySummaryLevel = 'major' | 'minor';\n\nexport interface ReleaseHistorySummaryNode {\n  readonly id: string;\n  readonly groupKey: string;\n  readonly level: ReleaseHistorySummaryLevel;\n  readonly entry: ReleaseHistoryEntry;\n  readonly childCount: number;\n  readonly children: readonly ReleaseHistorySummaryNode[];\n}\n\nexport const releaseHistorySummary: readonly ReleaseHistorySummaryNode[] = ${JSON.stringify(releaseHistoryTree.summary, null, 2)};\n`;
const loadersContent = buildLoadersContent(releaseHistoryTree.patchGroups);

mkdirSync(dirname(outputPath), { recursive: true });
writeFileSync(outputPath, content, 'utf8');
writeFileSync(summaryOutputPath, summaryContent, 'utf8');
writeFileSync(loadersOutputPath, loadersContent, 'utf8');
writePatchGroupFiles(releaseHistoryTree.patchGroups);

console.log(`Generated ${relative(projectRoot, outputPath)} with site version "${version}" from ${source}.`);
console.log(`Generated lazy release history in ${relative(projectRoot, summaryOutputPath)}.`);

function readGitTagVersion() {
  try {
    return execFileSync(
      'git',
      [
        '-c',
        `safe.directory=${projectRoot.replace(/\\/g, '/')}`,
        'describe',
        '--tags',
        '--abbrev=0'
      ],
      {
        cwd: projectRoot,
        encoding: 'utf8',
        stdio: ['ignore', 'pipe', 'ignore']
      }
    ).trim();
  } catch {
    return null;
  }
}

function normalizeReleaseHistory(history) {
  if (!Array.isArray(history)) {
    return [];
  }

  return history.map((entry, index) => normalizeReleaseHistoryEntry(entry, index));
}

function normalizeReleaseHistoryEntry(entry, index) {
  if (!entry || typeof entry !== 'object') {
    throw new Error(`release-version.json history entry ${index} must be an object.`);
  }

  const version = normalizeReleaseVersion(entry.version);
  const releasedAt = typeof entry.releasedAt === 'string' ? entry.releasedAt.trim() : '';
  if (!/^[0-9]{4}-[0-9]{2}-[0-9]{2}$/.test(releasedAt)) {
    throw new Error(`release-version.json history entry ${version} must have a YYYY-MM-DD releasedAt date.`);
  }

  if (!entry.labels || typeof entry.labels !== 'object' || Array.isArray(entry.labels)) {
    throw new Error(`release-version.json history entry ${version} must have localized labels.`);
  }

  return {
    version,
    releasedAt,
    labels: Object.fromEntries(Object.entries(entry.labels)
      .map(([language, label]) => [language.trim().toLowerCase(), typeof label === 'string' ? label.trim() : ''])
      .filter(([language, label]) => language.length === 2 && label.length > 0))
  };
}

function buildReleaseHistoryTree(history) {
  const groupsByMajor = new Map();

  for (const entry of history) {
    const parts = parseVersion(entry.version);
    const majorGroup = getOrCreateMajorGroup(groupsByMajor, parts.major);

    if (parts.minor === 0 && parts.patch === 0) {
      majorGroup.entry = entry;
      continue;
    }

    if (parts.minor === 0) {
      majorGroup.patchEntries.push(entry);
      continue;
    }

    const minorGroup = getOrCreateMinorGroup(majorGroup, parts.minor);

    if (parts.patch === 0) {
      minorGroup.entry = entry;
      continue;
    }

    minorGroup.patchEntries.push(entry);
  }

  const patchGroups = [];
  const summary = Array.from(groupsByMajor.values())
    .sort((left, right) => right.major - left.major)
    .map((majorGroup) => {
      const majorPatchEntries = sortEntriesDescending(majorGroup.patchEntries);
      if (majorPatchEntries.length > 0) {
        patchGroups.push({ groupKey: `${majorGroup.major}.0`, entries: majorPatchEntries });
      }

      const minorNodes = Array.from(majorGroup.minors.values())
        .filter((minorGroup) => minorGroup.entry || minorGroup.patchEntries.length > 0)
        .sort((left, right) => right.minor - left.minor)
        .map((minorGroup) => {
          const minorEntry = minorGroup.entry ?? minorGroup.patchEntries[0];
          const groupKey = `${majorGroup.major}.${minorGroup.minor}`;
          const patchEntries = sortEntriesDescending(minorGroup.patchEntries);

          if (patchEntries.length > 0) {
            patchGroups.push({ groupKey, entries: patchEntries });
          }

          return {
            id: `minor-${majorGroup.major}-${minorGroup.minor}`,
            groupKey,
            level: 'minor',
            entry: minorEntry,
            childCount: patchEntries.length,
            children: []
          };
        });

      const majorEntry = majorGroup.entry ?? minorNodes[0]?.entry;

      return {
        id: `major-${majorGroup.major}`,
        groupKey: `${majorGroup.major}`,
        level: 'major',
        entry: majorEntry,
        childCount: majorPatchEntries.length + minorNodes.reduce((total, minorNode) => total + 1 + minorNode.childCount, 0),
        children: minorNodes
      };
    })
    .filter((majorNode) => !!majorNode.entry);

  return { summary, patchGroups };
}

function getOrCreateMajorGroup(groupsByMajor, major) {
  const existingGroup = groupsByMajor.get(major);
  if (existingGroup) {
    return existingGroup;
  }

  const group = {
    major,
    entry: null,
    minors: new Map(),
    patchEntries: []
  };
  groupsByMajor.set(major, group);
  return group;
}

function getOrCreateMinorGroup(majorGroup, minor) {
  const existingGroup = majorGroup.minors.get(minor);
  if (existingGroup) {
    return existingGroup;
  }

  const group = {
    minor,
    entry: null,
    patchEntries: []
  };
  majorGroup.minors.set(minor, group);
  return group;
}

function sortEntriesDescending(entries) {
  return [...entries].sort((left, right) => compareVersions(right.version, left.version));
}

function parseVersion(version) {
  const parts = version.split('.').map((part) => Number.parseInt(part, 10));

  return {
    major: Number.isFinite(parts[0]) ? parts[0] : 0,
    minor: Number.isFinite(parts[1]) ? parts[1] : 0,
    patch: Number.isFinite(parts[2]) ? parts[2] : 0
  };
}

function compareVersions(left, right) {
  const leftParts = parseVersion(left);
  const rightParts = parseVersion(right);

  if (leftParts.major !== rightParts.major) {
    return leftParts.major - rightParts.major;
  }

  if (leftParts.minor !== rightParts.minor) {
    return leftParts.minor - rightParts.minor;
  }

  return leftParts.patch - rightParts.patch;
}

function buildLoadersContent(patchGroups) {
  const loaderEntries = patchGroups
    .sort((left, right) => compareVersions(right.groupKey, left.groupKey))
    .map((group) => {
      const moduleName = toPatchGroupModuleName(group.groupKey);
      return `  ${JSON.stringify(group.groupKey)}: () => import('./release-history-groups/${moduleName}').then((module) => module.releaseHistoryEntries)`;
    });

  return `// This file is generated by scripts/generate-version.mjs.\nimport type { ReleaseHistoryEntry } from './version.generated';\n\nexport const releaseHistoryPatchLoaders: Readonly<Record<string, () => Promise<readonly ReleaseHistoryEntry[]>>> = {\n${loaderEntries.join(',\n')}\n};\n`;
}

function writePatchGroupFiles(patchGroups) {
  rmSync(groupsOutputDirectory, { recursive: true, force: true });
  mkdirSync(groupsOutputDirectory, { recursive: true });

  for (const group of patchGroups) {
    const filePath = resolve(groupsOutputDirectory, `${toPatchGroupModuleName(group.groupKey)}.ts`);
    const groupContent = `// This file is generated by scripts/generate-version.mjs.\nimport type { ReleaseHistoryEntry } from '../version.generated';\n\nexport const releaseHistoryEntries: readonly ReleaseHistoryEntry[] = ${JSON.stringify(group.entries, null, 2)};\n`;
    writeFileSync(filePath, groupContent, 'utf8');
  }
}

function toPatchGroupModuleName(groupKey) {
  return `release-history-${groupKey.replace(/[^0-9]+/g, '-')}.generated`;
}

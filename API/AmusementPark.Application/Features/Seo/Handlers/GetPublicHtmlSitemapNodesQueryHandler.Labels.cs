    private static string Label(string language, string key)
    {
        if (SiteLabels.TryGetValue(language, out IReadOnlyDictionary<string, string>? labels)
            && labels.TryGetValue(key, out string? label))
        {
            return label;
        }

        return SiteLabels["en"].TryGetValue(key, out string? fallback) ? fallback : key;
    }
